package Unidad1;
public class Ejercicio14 {
    public static void main(String[] args) {
        System.out.println("Tabla de multiplicar");
        
        for (int i = 5; i <= 5; i++) {
            System.out.println("Tabla del " + i);
            for (int j = 1; j <= 10; j++) {
                System.out.println(i + " x " + j + " = " + i * j);
            }
        }
        
    }
}
